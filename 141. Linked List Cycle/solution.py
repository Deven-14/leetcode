# Definition for singly-linked list.
# class ListNode:
#     def __init__(self, x):
#         self.val = x
#         self.next = None

class Solution:
    def hasCycle(self, head: Optional[ListNode]) -> bool:
        visited = set()

        node = head
        while node != None and node not in visited:
            visited.add(node)
            node = node.next
        
        if node in visited:
            return True
        
        return False


